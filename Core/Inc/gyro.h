#ifndef GYRO_H
#define GYRO_H
#include "main.h"

#define BUF_SIZE 32

typedef struct
{
    /*---------------------------------------------------------------------------------------------------------------
                                                欧拉角数据 (单位：度)
    ---------------------------------------------------------------------------------------------------------------*/
    float pitch;    ///< 俯仰角 (Pitch) 绕X轴旋转角度，前后倾斜 [-180°, +180°]
    float yaw;      ///< 偏航角 (Yaw) 绕Z轴旋转角度，左右转向 [-180°, +180°]
    float roll;     ///< 横滚角 (Roll) 绕Y轴旋转角度，左右倾斜 [-180°, +180°]
    
    /*---------------------------------------------------------------------------------------------------------------
                                            加速度数据 (单位：m/s² 或 g)
    ---------------------------------------------------------------------------------------------------------------*/
    float ACC_X;    ///< X轴加速度 (前后方向) 正值表示向前加速
    float ACC_Y;    ///< Y轴加速度 (左右方向) 正值表示向右加速  
    float ACC_Z;    ///< Z轴加速度 (上下方向) 正值表示向上加速，静止时约为9.8m/s²
    
    /*---------------------------------------------------------------------------------------------------------------
                                            角速度数据 (单位：°/s 或 rad/s)
    ---------------------------------------------------------------------------------------------------------------*/
    float GYRO_X;   ///< X轴角速度 (俯仰角速度) 正值表示抬头方向旋转
    float GYRO_Y;   ///< Y轴角速度 (横滚角速度) 正值表示向右倾斜方向旋转
    float GYRO_Z;   ///< Z轴角速度 (偏航角速度) 正值表示逆时针旋转
    
} GYROSCOPE;

void ruifen_decoded(uint8_t data[BUF_SIZE], GYROSCOPE *gyro);
void witdecoded(uint8_t data[BUF_SIZE], GYROSCOPE *gyro);
void Wit_Gyro_Restart(void);
extern uint8_t Gyro_data[BUF_SIZE];
extern GYROSCOPE Wit_Gyro;

#endif

